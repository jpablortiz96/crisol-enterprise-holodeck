def _attribution_check() -> dict[str, Any]:
    findings = []
    for path in _repository_text_files():
        for line_number, line in enumerate(
            path.read_text(encoding="utf-8", errors="ignore").splitlines(),
            start=1,
        ):
            lower_line = line.lower()
            for pattern in ATTRIBUTION_PATTERNS:
                if pattern in lower_line:
                    findings.append(
                        {
                            "file": path.relative_to(REPOSITORY_ROOT).as_posix(),
                            "line": line_number,
                            "term": pattern,
                        }
                    )
    return _check(
        "attribution_scan",
        "pass" if not findings else "fail",
        "Repository attribution scan passed."
        if not findings
        else "Prohibited attribution language was detected.",
        {"finding_count": len(findings), "findings": findings},
        "Remove prohibited attribution language." if findings else None,
    )


def _env_protection_check() -> dict[str, Any]:
    ignored = _git_check_ignore(".env")
    tracked = (
        subprocess.run(
            ["git", "ls-files", "--error-unmatch", ".env"],
            cwd=REPOSITORY_ROOT,
            capture_output=True,
            text=True,
            check=False,
        ).returncode
        == 0
    )
    valid = ignored and not tracked
    return _check(
        "env_protection",
        "pass" if valid else "fail",
        "The local .env file is ignored and untracked."
        if valid
        else "The local .env file is not safely protected.",
        {"ignored": ignored, "tracked": tracked},
        "Add .env to .gitignore and remove it from the Git index." if not valid else None,
    )


def _generated_ignore_check() -> dict[str, Any]:
    missing = [probe for probe in GENERATED_IGNORE_PROBES if not _git_check_ignore(probe)]
    return _check(
        "generated_folders_gitignore",
        "pass" if not missing else "fail",
        "Generated runtime and build paths are ignored."
        if not missing
        else "Generated paths are missing Git ignore coverage.",
        {"probes": len(GENERATED_IGNORE_PROBES), "missing": missing},
        "Add missing generated paths to .gitignore." if missing else None,
    )


def _docker_smoke_support_check() -> dict[str, Any]:
    artifacts_exist = all(
        path.exists()
        for path in (
            BACKEND_ROOT / "Dockerfile",
            BACKEND_ROOT / ".dockerignore",
            REPOSITORY_ROOT / "docs" / "DOCKER_SMOKE_TEST.md",
        )
    )
    if not artifacts_exist:
        return _check(
            "docker_smoke_support",
            "fail",
            "Docker smoke-test artifacts are incomplete.",
            {"artifacts_exist": False},
            "Restore the backend container files and Docker smoke-test guide.",
        )

    docker = shutil.which("docker")
    if not docker:
        return _check(
            "docker_smoke_support",
            "warn",
            "Docker artifacts are present; the Docker CLI is unavailable for a live smoke check.",
            {"artifacts_exist": True, "docker_cli": False, "daemon": False},
            "Run the documented Docker smoke test on a host with Docker available.",
        )

    result = subprocess.run(
        [docker, "info", "--format", "{{.ServerVersion}}"],
        cwd=REPOSITORY_ROOT,
        capture_output=True,
        text=True,
        timeout=10,
        check=False,
    )
    if result.returncode != 0:
        return _check(
            "docker_smoke_support",
            "warn",
            "Docker artifacts are present; the local Docker daemon is unavailable.",
            {"artifacts_exist": True, "docker_cli": True, "daemon": False},
            "Start Docker and run the documented container smoke test.",
        )
    return _check(
        "docker_smoke_support",
        "pass",
        "Docker artifacts and local daemon are available for smoke testing.",
        {
            "artifacts_exist": True,
            "docker_cli": True,
            "daemon": True,
            "server_version": result.stdout.strip(),
        },
    )


