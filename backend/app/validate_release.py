def _git_check_ignore(relative_path: str) -> bool:
    return (
        subprocess.run(
            ["git", "check-ignore", "-q", "--no-index", relative_path],
            cwd=REPOSITORY_ROOT,
            capture_output=True,
            text=True,
            check=False,
        ).returncode
        == 0
    )


def _repository_text_files() -> list[Path]:
    ignored = {
        ".git",
        ".next",
        ".venv",
        "__pycache__",
        "node_modules",
        ".crisol_sessions",
        ".crisol_audio",
        ".crisol_telemetry",
        ".crisol_exports",
    }
    suffixes = {".css", ".js", ".json", ".jsx", ".md", ".py", ".ts", ".tsx", ".txt"}
    return [
        path
        for path in REPOSITORY_ROOT.rglob("*")
        if path.is_file()
        and not path.is_symlink()
        and path.name != ".env"
        and path.suffix.lower() in suffixes
        and not any(part in ignored for part in path.relative_to(REPOSITORY_ROOT).parts)
        and path.stat().st_size <= 2_000_000
    ]


def _check(
    check_id: str,
    status: str,
    summary: str,
    details: dict[str, Any],
    recommendation: str | None = None,
) -> dict[str, Any]:
    return {
        "check_id": check_id,
        "status": status,
        "summary": summary,
        "details": details,
        "recommendation": recommendation,
    }


def main() -> None:
    parser = argparse.ArgumentParser(description="Validate the CRISOL release candidate.")
    parser.add_argument("--json", action="store_true", help="Print the complete JSON report.")
    arguments = parser.parse_args()
    report = run_release_validation()

    if arguments.json:
        print(json.dumps(report, indent=2))
    else:
        label = "PASS" if report["release_candidate"] else "FAIL"
        print(f"{label} release validation")
        print(f"score: {report['score']}")
        print(f"blocking_issues: {len(report['blocking_issues'])}")
        print(f"warnings: {len(report['warnings'])}")

    if not report["release_candidate"]:
        raise SystemExit(1)


if __name__ == "__main__":
    main()
