def _replay_check() -> dict[str, Any]:
    with examples_enabled_for_validation():
        session = run_simulation(
            role_id="ROLE-SRE",
            scenario_seed="SCN-SRE-001",
            auto_mode=True,
        )
        node = next(
            item for item in session["timeline"]["nodes"] if item["turn_number"] == 1
        )
        branch = branch_from_session(
            session["session_id"],
            node["node_id"],
            "Freeze writes and identify primary database writer",
        )
    valid = bool(
        branch.get("new_session_id")
        and branch.get("comparison")
        and branch.get("citations")
    )
    return _check(
        "replay_branch",
        "pass" if valid else "fail",
        "Time-Travel Replay produced a cited branch comparison."
        if valid
        else "Time-Travel Replay branch contract failed.",
        {
            "source_session_id": session["session_id"],
            "branch_session_id": branch.get("new_session_id"),
            "citation_count": len(branch.get("citations", [])),
        },
        "Repair deterministic replay branch generation." if not valid else None,
    )


