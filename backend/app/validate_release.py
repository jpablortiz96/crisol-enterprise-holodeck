def run_release_validation() -> dict[str, Any]:
    checks: list[dict[str, Any]] = []

    for check_id, module in VALIDATION_MODULES:
        checks.append(_run_validation_module(check_id, module))

    checks.extend(
        [
            _endpoint_registry_check(),
            _scenario_library_check(),
            _telemetry_check(),
            _mcp_tools_check(),
            _replay_check(),
            _security_check(),
            _sensitive_data_check(),
            _product_language_check(),
            _attribution_check(),
            _env_protection_check(),
            _generated_ignore_check(),
            _docker_smoke_support_check(),
            _external_integration_check(),
        ]
    )

    blocking_issues = [
        check["summary"] for check in checks if check["status"] == "fail"
    ]
    warnings = [check["summary"] for check in checks if check["status"] == "warn"]
    score = max(0, 100 - len(blocking_issues) * 15 - len(warnings) * 3)
    release_candidate = not blocking_issues and score >= 90
    status = "fail" if blocking_issues else "warn" if warnings else "pass"
    next_steps = [
        check["recommendation"]
        for check in checks
        if check["status"] != "pass" and check.get("recommendation")
    ]
    if not next_steps:
        next_steps = ["Tag the verified release candidate and execute the deployment smoke test."]

    return {
        "status": status,
        "release_candidate": release_candidate,
        "checks": checks,
        "score": score,
        "blocking_issues": blocking_issues,
        "warnings": warnings,
        "recommended_next_steps": next_steps,
    }


def _run_validation_module(check_id: str, module: str) -> dict[str, Any]:
    result = subprocess.run(
        [sys.executable, "-m", module],
        cwd=BACKEND_ROOT,
        capture_output=True,
        text=True,
        timeout=180,
        check=False,
    )
    output_lines = [
        line.strip()
        for line in (result.stdout + "\n" + result.stderr).splitlines()
        if line.strip()
    ]
    passed = result.returncode == 0
    return _check(
        f"validation_{check_id}",
        "pass" if passed else "fail",
        f"{check_id.replace('_', ' ').title()} validation passed."
        if passed
        else f"{check_id.replace('_', ' ').title()} validation failed.",
        {
            "module": module,
            "return_code": result.returncode,
            "last_output": output_lines[-1] if output_lines else None,
        },
        f"Run python -m {module} and resolve the reported failure." if not passed else None,
    )


def _endpoint_registry_check() -> dict[str, Any]:
    from app.main import app

    registered = {route.path for route in app.routes}
    missing = sorted(REQUIRED_ENDPOINTS - registered)
    return _check(
        "endpoint_registry",
        "pass" if not missing else "fail",
        "Required product endpoints are registered."
        if not missing
        else "Required product endpoints are missing.",
        {"registered_count": len(registered), "missing": missing},
        "Restore every required release endpoint." if missing else None,
    )


def _scenario_library_check() -> dict[str, Any]:
    scenarios = list_scenarios()
    status = workspace_status()
    expected_active = status["scenario_count"] + (
        5 if status["load_examples"] else 0
    )
    valid = len(scenarios) == expected_active and all(
        scenario.get("data_classification") == "sanitized-training"
        for scenario in scenarios
    )
    return _check(
        "scenario_library",
        "pass" if valid else "fail",
        "Scenario Library matches the configured workspace and optional example mode."
        if valid
        else "Scenario Library count or classification does not match workspace status.",
        {
            "active_scenario_count": len(scenarios),
            "workspace_scenario_count": status["scenario_count"],
            "examples_enabled": status["load_examples"],
            "workspace_empty": status["is_empty"],
        },
        "Repair workspace scenario discovery and example-mode configuration."
        if not valid
        else None,
    )


def _telemetry_check() -> dict[str, Any]:
    summary = telemetry_summary()
    valid = (
        summary.get("status") == "active"
        and summary.get("data_mode") == "sanitized-training"
        and summary.get("production_changes") is False
        and summary.get("event_count", 0) > 0
    )
    return _check(
        "telemetry_summary",
        "pass" if valid else "fail",
        "Local telemetry is active and sanitized."
        if valid
        else "Telemetry summary does not meet the release contract.",
        {
            "event_count": summary.get("event_count", 0),
            "data_mode": summary.get("data_mode"),
            "production_changes": summary.get("production_changes"),
        },
        "Run a validation scenario and verify telemetry allowlisting." if not valid else None,
    )


